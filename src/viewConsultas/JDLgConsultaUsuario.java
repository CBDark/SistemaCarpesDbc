    private void jBtnConsultarDbcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarDbcActionPerformed
        if (jTxtNomeDbc.getText().equals("") && jTxtCpfDbc.getText().equals("")){
            List lista = usuariosDAO.listNome(jTxtNomeDbc.getText());
            usuarioControle.setList(lista);
        } else {
            if (! jTxtNomeDbc.getText().equals("") && ! jTxtCpfDbc.getText().equals("")){
                List lista = usuariosDAO.listNomeCpf(jTxtNomeDbc.getText(), jTxtCpfDbc.getText());
                usuarioControle.setList(lista);
            } else {
                if (! jTxtNomeDbc.getText().equals("")){
                    List lista = usuariosDAO.listNome(jTxtNomeDbc.getText());
                    usuarioControle.setList(lista);
                }
                if(! jTxtCpfDbc.getText().equals("")){
                    List lista = usuariosDAO.listCpf(jTxtCpfDbc.getText());
                    usuarioControle.setList(lista); 
                }
            } 
        }
        
    }//GEN-LAST:event_jBtnConsultarDbcActionPerformed

