     public UsuariosDbc viewBean() {
         /*SetarId*/
        UsuariosDbc usuariosDbc = new UsuariosDbc();
        int id = UtilDbc.strInt(JTxtCodigo_dbc.getText());
        usuariosDbc.setIdusuariosDbc(id);
        /*SetarDatas*/
        Date dataNascimento = UtilDbc.strDate(jFmtData_dbc.getText());
        usuariosDbc.setDataNascDbc(dataNascimento);
        /*SetarTextos*/
        usuariosDbc.setNomeDbc(JTxtNome_dbc.getText());
        usuariosDbc.setEmailDbc(JTxtEmail_dbc.getText());
        usuariosDbc.setCpfDbc(jFmtCpf_dbc.getText());
        usuariosDbc.setSenhaDbc(jPwfSenha_dbc.getText());
        /*SetarCombo*/
        if ((JCboSexo_dbc.getSelectedIndex() == 1)) {
            usuariosDbc.setSexoDbc("Masculino");
        }else {
            usuariosDbc.setSexoDbc("Feminino");
        }
        
        if(jChbAtivo_dbc.isSelected() == true){
        usuariosDbc.setAtivoDbc("S");
        } else {
        usuariosDbc.setAtivoDbc("N");
        }
        return usuariosDbc;

    }

    public void beanView(UsuariosDbc usuariosDbc) {
        /*SetarId*/
        String intStr = UtilDbc.intStr(usuariosDbc.getIdusuariosDbc());
        JTxtCodigo_dbc.setText(intStr);
        /*SetarDatas*/
        String dataNascimento = UtilDbc.dateStr(usuariosDbc.getDataNascDbc());
        jFmtData_dbc.setText(dataNascimento);
        
        JTxtNome_dbc.setText(usuariosDbc.getNomeDbc());
        JTxtEmail_dbc.setText(usuariosDbc.getEmailDbc());
        jFmtCpf_dbc.setText(usuariosDbc.getCpfDbc());
        jPwfSenha_dbc.setText(usuariosDbc.getSenhaDbc());
        
        /*SetarCombos*/
        String sexo = usuariosDbc.getSexoDbc();
        if(sexo.equals("M")){
            JCboSexo_dbc.setSelectedIndex(0);
        }else {
            JCboSexo_dbc.setSelectedIndex(1);
        }
        
        if(usuariosDbc.getAtivoDbc().equals("S") == true){
            jChbAtivo_dbc.setSelected(true);
        } else {
            jChbAtivo_dbc.setSelected(false);
        }
        
    }
    
