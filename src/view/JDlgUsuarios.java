    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
    UsuariosDbc usuarioDbc = viewBean();
    UsuariosDAO usuarioDAO = new UsuariosDAO();
    
    if (incl == true) {
        usuarioDAO.insert(usuarioDbc);
        UtilDbc.mensagem("Registro incluido com sucesso.");
    } else {
        usuarioDAO.update(usuarioDbc);
        UtilDbc.mensagem("Registro alterado com sucesso.");
        }
    
        UtilDbc.habilitar(false, jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,JTxtCodigo_dbc,jFmtCpf_dbc,jChbAtivo_dbc,JTxtNome_dbc,jFmtData_dbc,JTxtEmail_dbc,JCboSexo_dbc,jPwfSenha_dbc);
        UtilDbc.habilitar(true, jBtnIncluir,jBtnPesquisar);
        UtilDbc.limparCampos(JTxtCodigo_dbc,jFmtCpf_dbc,jChbAtivo_dbc,JTxtNome_dbc,jFmtData_dbc,JTxtEmail_dbc,JCboSexo_dbc,jPwfSenha_dbc);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

