    public FuncionarioDbc viewBean() {
        /*SetarId*/
        FuncionarioDbc funcionarioDbc = new FuncionarioDbc();
        int id = UtilDbc.strInt(jFmtCodigoDbc.getText());
        funcionarioDbc.setIdfuncionarioDbc(id);

        /*SetarDoble*/
        double salario = UtilDbc.strDouble(jFmtSalarioDbc.getText());
        funcionarioDbc.setSalarioDbc(salario);
        /*SetarCombo*/
        if (jCboNivelDbc.getSelectedIndex() == 0) {
            funcionarioDbc.setSexoDbc("Masculino");
        } else {
            funcionarioDbc.setSexoDbc("Feminino");
        }
        if (jCboDerpDBC.getSelectedIndex() == 0) {
            funcionarioDbc.setDepartamentoDbc("Vendas");
        } else{
            funcionarioDbc.setDepartamentoDbc("Estoque");     
        }
        /*SetarTexto*/
        funcionarioDbc.setNomeDbc(jTxtNomeDbc.getText());
        funcionarioDbc.setNumeroTelefoneDbc(jFmtNumeroTelDbc.getText());
        funcionarioDbc.setEmailDbc(jTxtEmailDbc.getText());
        funcionarioDbc.setCpfDbc(jFmtCpfDbc.getText()); 
        
       

        return funcionarioDbc;
    }

    public void beanView(FuncionarioDbc funcionarioDbc) {
        /*SetarId*/
        String intStr = UtilDbc.intStr(funcionarioDbc.getIdfuncionarioDbc());
        jFmtCodigoDbc.setText(intStr);
        /*SetarDouble*/

        String salarios = UtilDbc.doubleStr(funcionarioDbc.getSalarioDbc());
        jFmtSalarioDbc.setText(salarios);
        /*SetarTexto*/
        jTxtNomeDbc.setText(funcionarioDbc.getNomeDbc());
        jTxtEmailDbc.setText(funcionarioDbc.getEmailDbc()); 
        jFmtCpfDbc.setText(funcionarioDbc.getCpfDbc());
        jFmtNumeroTelDbc.setText(funcionarioDbc.getNumeroTelefoneDbc());
        String sex = funcionarioDbc.getSexoDbc();
        if(sex.equals("Masculino")){
            jCboNivelDbc.setSelectedIndex(0);
        }else {
            jCboNivelDbc.setSelectedIndex(1);
        }
        String set = funcionarioDbc.getDepartamentoDbc();
        if(set.equals("estoque")){
            jCboDerpDBC.setSelectedIndex(0);
        }else {
           jCboDerpDBC.setSelectedIndex(1);
        }
        
        
    }
   
  
     
