    private void jBtnOk1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOk1ActionPerformed
        funcionarioDbc = viewBean();
        funcionarioDAO = new FuncionarioDAO();
        if (incluindo) {
            funcionarioDAO.insert(funcionarioDbc);
            
            List lista = funcionarioDAO.listAll();
            jDlgFuncionariosNovo.funcionarioControle.setList(lista);
            
            UtilDbc.mensagem("Registro incluído com sucesso.");
            
        } else {
            funcionarioDAO.update(funcionarioDbc);
            
            List lista = funcionarioDAO.listAll();
            jDlgFuncionariosNovo.funcionarioControle.setList(lista);
            
            UtilDbc.mensagem("Registro alterado com sucesso.");
        }
        UtilDbc.limparCampos(jFmtCodigoDbc, jFmtCpfDbc,  jFmtNumeroTelDbc, jFmtSalarioDbc, jCboDerpDBC, jCboNivelDbc, jTxtEmailDbc, jTxtNomeDbc);
        this.dispose();
    }//GEN-LAST:event_jBtnOk1ActionPerformed

